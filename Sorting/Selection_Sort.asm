.MODEL SMALLL


.STACK 100H

.DATA
A DB 5,3,2,1


.CODE
SELECT PROC 

PUSH BX
PUSH CX
PUSH DX
PUSH SI
DEC BX
JE END_SORT
MOV DX , SI

SORT_LOOP:
    MOV SI, DX
    MOV CX , BX
    MOV DI , SI
    MOV AL, [DI]

FIND_BIG:
    INC SI
    CMP [SI],AL
    JNG NEXT
    MOV DI , SI
    MOV AL , [DI]
    
NEXT:
    LOOP FIND_BIG
    
    CALL SWAP
    DEC BX
    JNE SORT_LOOP
END_SORT:

    POP SI
    POP DX
    POP CX
    POP BX
    RET
SELECT ENDP

SWAP PROC
    PUSH AX
    MOV AL , [SI]
    XCHG AL, [DI]
    MOV [SI],AL
    POP AX
    RET
  
SWAP ENDP
MAIN PROC
    MOV AX , @DATA
    MOV DS , AX
    LEA SI ,A
    MOV BX , 5
    CALL SELECT
    
    MOV AH , 4CH
    INT 21H